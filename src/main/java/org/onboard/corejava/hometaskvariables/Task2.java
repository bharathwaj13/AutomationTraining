package org.onboard.corejava.hometaskvariables;

public class Task2 {

    /**
     * Daniel has 3.5 apples and Amber has 2.5 apples. How many apples do Daniel and Amber have together?
     */

    public static void main(String[] args) {
        float danApples = 3.5f;
        float ambApples = 2.5f;
        float sumOfApples = danApples + ambApples;
        System.out.println("Sum Of Apples: " + sumOfApples);
    }
}
